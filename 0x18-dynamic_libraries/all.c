/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: Addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: successful when subtracted
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: Sucessful multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - module two integers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}

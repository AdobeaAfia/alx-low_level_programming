#include <stdio.h>

/*
 * add - addition of integers
 * @a: first integer
 * @b: second integer
 * Return: successful addition
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: divide when successful
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - module two integers
 * @a: first int
 * @b: second int
 * Return: module when successful
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiply when successful
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: subtraction when successful
 */
int sub(int a, int b)
{
	return (a - b);
}
